static uint8_t GaloisMultiply(uint8_t value)
{
    if (value >> 7 != 0) {
        value = (value << 1);
        return (value ^ 0x1b);
    } else {
        return (value << 1);
    }
}

void DLMS_Aes1Encrypt(const uint8_t *data, const uint8_t *key, uint8_t *data_out)
{
    uint8_t key_tmp[16];
    memmove(key_tmp, key, 16);
    memmove(data_out, data, 16);
    uint8_t buf1, buf2, buf3, buf4, round, i;
    for (round = 0; round < 10; ++round)
    {
        for (i = 0; i < 16; ++i)
        {
            data_out[i] = S_BOX[(data_out[i] ^ key_tmp[i]) & 0xFF];
        }
        // shift rows
        buf1 = data_out[1];
        data_out[1] = data_out[5];
        data_out[5] = data_out[9];
        data_out[9] = data_out[13];
        data_out[13] = buf1;

        buf1 = data_out[2];
        buf2 = data_out[6];
        data_out[2] = data_out[10];
        data_out[6] = data_out[14];
        data_out[10] = buf1;
        data_out[14] = buf2;

        buf1 = data_out[15];
        data_out[15] = data_out[11];
        data_out[11] = data_out[7];
        data_out[7] = data_out[3];
        data_out[3] = buf1;

        if (round < 9)
        {
            for (i = 0; i < 4; i++)
            {
                buf4 = (i << 2);
                buf1 = (data_out[buf4] ^ data_out[buf4 + 1]
                    ^ data_out[buf4 + 2]
                    ^ data_out[buf4 + 3]);
                buf2 = data_out[buf4];
                buf3 = (data_out[buf4]
                    ^ data_out[buf4 + 1]);
                buf3 = GaloisMultiply(buf3);
                data_out[buf4] =
                    (data_out[buf4] ^ buf3 ^ buf1);
                buf3 = (data_out[buf4 + 1]
                    ^ data_out[buf4 + 2]);
                buf3 = GaloisMultiply(buf3);
                data_out[buf4 + 1] =
                    (data_out[buf4 + 1] ^ buf3 ^ buf1);
                buf3 = (data_out[buf4 + 2]
                    ^ data_out[buf4 + 3]);
                buf3 = GaloisMultiply(buf3);
                data_out[buf4 + 2] = (data_out[buf4 + 2] ^ buf3 ^ buf1);
                buf3 = (data_out[buf4 + 3] ^ buf2);
                buf3 = GaloisMultiply(buf3);
                data_out[buf4 + 3] = (data_out[buf4 + 3] ^ buf3 ^ buf1);
            }
        }

        key_tmp[0] = (S_BOX[key_tmp[13] & 0xFF] ^ key_tmp[0] ^ R_CON[round]);
        key_tmp[1] = (S_BOX[key_tmp[14] & 0xFF] ^ key_tmp[1]);
        key_tmp[2] = (S_BOX[key_tmp[15] & 0xFF] ^ key_tmp[2]);
        key_tmp[3] = (S_BOX[key_tmp[12] & 0xFF] ^ key_tmp[3]);
        for (i = 4; i < 16; i++)
        {
            key_tmp[i] = (key_tmp[i] ^ key_tmp[i - 4]);
        }
    }

    for (i = 0; i < 16; i++)
    {
        data_out[i] = (data_out[i] ^ key_tmp[i]);
    }
}
